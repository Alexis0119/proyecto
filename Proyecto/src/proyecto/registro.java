    void cargar (String valor){
        String [] titulos={"Clave","Nombre","Apellido_Paterno","Apellido_Materno","Sexo","Edad","Telefono"};
        String [] registros = new String[7];
        
        String sql = "SELECT * FROM datos where Clave LIKE '%"+valor+"%'";
        
        model = new DefaultTableModel(null,titulos);
        
        conectar cc= new conectar();
        Connection cn = cc.conexion();
        
        try {
                    Statement st = cn.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    
                    while(rs.next()){
                        registros[0]= rs.getString("Clave");
                        registros[1]= rs.getString("Apellido_Paterno");
                        registros[2]= rs.getString("Apellido_Materno");
                        registros[3]= rs.getString("Nombre");
                        registros[4]= rs.getString("Sexo");
                        registros[5]= rs.getString("Edad");
                        registros[6]= rs.getString("Telefono");
                        model.addRow(registros);
                    }
                    tdatos.setModel(model);                   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }       
    }
    
