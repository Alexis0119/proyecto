    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        conectar cc = new conectar();
        Connection cn = cc.conexion();
        String Clave,Nombre,Apellido_Paterno,Apellido_Materno,Sexo,Edad,Telefono;
        String sql;
        Clave = tclave.getText();
        Nombre = tnom.getText();
        Apellido_Paterno = tap.getText();
        Apellido_Materno = tam.getText();
        Sexo = tsex.getText();
        Edad = tedad.getText();
        Telefono = ttel.getText();
        sql="INSERT INTO datos (Clave,Apellido_Paterno,Apellido_Materno,Nombre,Sexo,Edad,Telefono) VALUES (?,?,?,?,?,?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1,Clave);
            pst.setString(2,Apellido_Paterno);
            pst.setString(3,Apellido_Materno);
            pst.setString(4,Nombre);
            pst.setString(5,Sexo);
            pst.setString(6,Edad);
            pst.setString(7,Telefono);
            int n= pst.executeUpdate();
            if (n>0){
                JOptionPane.showMessageDialog(null, "Registro guardado con exito");
                bloquear();
                cargar("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(registro.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

